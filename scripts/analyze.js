function parsePath(d) {
  const tokens = d.match(/[MmLlZz]|[-+]?(?:\d*\.\d+|\d+)(?:[eE][-+]?\d+)?/g) || [];
  const points = [];
  let x = 0, y = 0, i = 0;
  while (i < tokens.length) {
    const cmd = tokens[i++];
    if (cmd === "M" || cmd === "m") {
      let nx = parseFloat(tokens[i++]), ny = parseFloat(tokens[i++]);
      if (cmd === "m") { nx += x; ny += y; }
      x = nx; y = ny; points.push([x, y]);
    } else if (cmd === "l" || cmd === "L") {
      let nx = parseFloat(tokens[i++]), ny = parseFloat(tokens[i++]);
      if (cmd === "l") { nx += x; ny += y; }
      x = nx; y = ny; points.push([x, y]);
    } else break;
  }
  return points;
}

